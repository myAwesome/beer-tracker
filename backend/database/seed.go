func Seed(db *gorm.DB) {
	styles := make([]models.Style, 0, len(styleNames))
	for _, name := range styleNames {
		styles = append(styles, models.Style{Name: strings.TrimSpace(name)})
	}

	result := db.Clauses(clause.OnConflict{DoNothing: true}).Create(&styles)
	if result.Error != nil {
		log.Printf("seed styles error: %v", result.Error)
		return
	}
	log.Printf("Seeded %d beer styles", result.RowsAffected)
}
